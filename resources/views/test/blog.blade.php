                    <h2 class="entry-title"><a href="http://nazonohito51.jugem.jp/?eid=61">ポーカー（テキサスホールデム）で遊びました</a></h2>
                </div>
                <div class="entry-body">
                    <div class="entry-description"><div class="jgm_entry_desc_mark"><div class="jugem_theme">JUGEMテーマ：<a href="http://jugem.jp/theme/c277/14/" target="_blank">ゲーム</a>
                            </div>
                            &nbsp;
                            <div style="text-align: center;"><img alt="" class="pict" height="271" src="http://img-cdn.jg.jugem.jp/074/3215149/20160124_1229683.jpg" width="400" />
                            </div>
                            <br />
                            ポーカーのテキサスホールデムというルールで遊びました。
                            <br />
                            通常のポーカーと違い、手札2枚・全員で共有する場のカード5枚で役を作って競うルールです。
                            <br />
                            役は通常のポーカーと同じ5枚で作るので、
                            <br />
                            都合２＋５＝7枚の中から5枚を選んで役を作ります。
                            <br />
                            <br />
                            テキサスホールデムではゲーム進行に合わせて
                            <br />
                            場のカードの枚数が０&rarr;３&rarr;４&rarr;５増えていきます。
                            <br />
                            参加人数が多い時は場のカードが増えるにしたがって状況が大きく変わったりするんですが、
                            <br />
                            脱落者が増えて人数が2人になるとあまり変わらず、
                            <br />
                            最初の手札2枚でだいたい勝負の流れが決まったりするんですね。
                            <br />
                            <br />
                            これって人数が多いと誰かしら役が出来るので<strong>より強い役で戦うという性質の勝負</strong>なんですが、
                            <br />
