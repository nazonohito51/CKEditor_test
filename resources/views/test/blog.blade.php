                                <li>メソッド呼び出し時の&amp;はサポートしなくなった。&amp;はメソッド定義側にのみ記述する。</li>
                                <li>break、continueへの引数に変数は使えなくなった（つか使えたのか・・・）</li>
                                <li>&#36;_GETや&#36;_POSTなどスーパーグローバル変数を引数に使うとFATAL ERRORとなった</li>
                                <li>ハッシュアルゴリズムからSalsa10とSalsa20が削除された</li>
                                <li>ob_startの3番目の引数がbooleanからintegerに代わった。</li>
                                <li>sqliteがPHP拡張モジュールからPECLに移動した。</li>
                                <li>pdo_mysqlがMySQLクライアントライブラリ4.1より前のバージョンとのリンクをサポートしなくなった！！！</li>
                                <li>htmlspecialcharsとhtmlentitiesのデフォルト文字セットがUTF-8になり、default_charsetがデフォルトにならなくなった。一般的にPHP5.4移行時は第3引数に明示的に変換前の文字エンコーディングを明示する必要がある。</li>
